package control_flow;

public class loop {
    public static void main(String[] args) {
        for (int i = 0; i<=10; i++) {
            System.out.println(i + " Hello");
        }

        // While loop
        int i = 0;
        while(i<=10) {
            System.out.println(i);
            i = i + 2;
        }
    }
}
