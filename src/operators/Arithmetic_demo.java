package operators;

public class Arithmetic_demo {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        System.out.println(a++);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
    }
}
