package decision;

public class ifElseIf_demo {
    public static void main(String args[]) {
        int number = 100;

        if( number == 10 ) {
            System.out.print("Grade F");
        }
        else if( number == 20 ) {
            System.out.print("Grade D");
        }
        else if( number == 30 ) {
            System.out.print("Grade C");
        }
        else {
            System.out.print("No Grade");
        }
    }
}
